returns trigger
language plpgsql
as $$
begin
  if tg_op='INSERT' and (new.reference_no is null or btrim(new.reference_no)='') then
    new.reference_no := public.generate_ticket_reference();
  end if;
  if new.status in ('resolved','closed') and new.resolved_at is null then
    new.resolved_at := now();
