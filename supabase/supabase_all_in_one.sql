create or replace function public.current_user_has_any_role(_roles public.app_role_code[])
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select case
    when auth.uid() is null then false
    else exists (
      select 1
      from public.user_roles ur
      join public.roles r on r.id = ur.role_id
      where ur.user_id = auth.uid()
        and r.code = any(_roles)
    )
  end;
$$;

create or replace function public.current_user_is_admin()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select public.current_user_has_any_role(array['admin']::public.app_role_code[]);
$$;

drop function if exists public.authenticate_admin_account(text, text);
create or replace function public.authenticate_admin_account(_username text, _password text)
returns table (
  admin_id uuid,
  username citext,
  email citext,
  display_name text,
  session_token text,
  expires_at timestamptz
)
language plpgsql
security definer
set search_path = public
as $$
declare
  _admin public.admin_accounts%rowtype;
  _session_token text;
  _expires_at timestamptz;
begin
  select *
  into _admin
  from public.admin_accounts aa
  where aa.is_active = true
    and lower(aa.username::text) = lower(trim(_username))
    and aa.password_hash = extensions.crypt(_password, aa.password_hash)
  limit 1;

  if not found then
    return;
  end if;

  _session_token := encode(extensions.gen_random_bytes(32), 'hex');
  _expires_at := now() + interval '12 hours';

  insert into public.admin_sessions (
    admin_id,
    token_hash,
    expires_at
  )
  values (
    _admin.id,
    encode(extensions.digest(_session_token, 'sha256'), 'hex'),
    _expires_at
  );

  return query
  select
    _admin.id,
    _admin.username,
    _admin.email,
    _admin.display_name,
    _session_token,
    _expires_at;
end;
$$;

create or replace function public.validate_admin_session_token(_session_token text)
returns table (
  admin_id uuid,
  username citext,
  email citext,
  display_name text,
  expires_at timestamptz
)
language plpgsql
security definer
set search_path = public
as $$
begin
  update public.admin_sessions
  set last_used_at = now()
  where token_hash = encode(extensions.digest(_session_token, 'sha256'), 'hex')
    and revoked_at is null
    and public.admin_sessions.expires_at > now();

  return query
  select
    aa.id,
    aa.username,
    aa.email,
    aa.display_name,
    s.expires_at
  from public.admin_sessions s
  join public.admin_accounts aa on aa.id = s.admin_id
  where s.token_hash = encode(extensions.digest(_session_token, 'sha256'), 'hex')
    and s.revoked_at is null
    and s.expires_at > now()
    and aa.is_active = true
  limit 1;
end;
$$;

create or replace function public.revoke_admin_session_token(_session_token text)
returns void
language plpgsql
security definer
set search_path = public
as $$
begin
  update public.admin_sessions
  set revoked_at = now()
  where token_hash = encode(extensions.digest(_session_token, 'sha256'), 'hex')
    and revoked_at is null;
end;
$$;

create or replace function public.create_admin_template_document(
  _session_token text,
  _name text,
  _description text,
  _template_description text
)
returns table (
  id uuid,
  name text,
  description text,
  template_url text,
  template_description text,
  sort_order integer,
  is_required boolean,
  is_active boolean,
  updated_at timestamptz
)
language plpgsql
security definer
set search_path = public
as $$
declare
  _next_sort_order integer;
  _admin_id uuid;
begin
  select vat.admin_id
  into _admin_id
  from public.validate_admin_session_token(_session_token) vat
  limit 1;

  if _admin_id is null then
    raise exception 'Admin account is not authorized.';
  end if;

  select coalesce(max(rdt.sort_order), 0) + 1
  into _next_sort_order
  from public.required_document_types rdt;

  return query
  insert into public.required_document_types (
    name,
    description,
    required_year,
    is_required,
    template_url,
    template_description,
    sort_order,
    is_active
  )
  values (
    trim(_name),
    coalesce(_description, ''),
    extract(year from now())::integer,
    true,
    '',
    coalesce(_template_description, ''),
    _next_sort_order,
    true
  )
  returning
    required_document_types.id,
    required_document_types.name,
    required_document_types.description,
    required_document_types.template_url,
    required_document_types.template_description,
    required_document_types.sort_order,
    required_document_types.is_required,
    required_document_types.is_active,
    required_document_types.updated_at;
end;
$$;

